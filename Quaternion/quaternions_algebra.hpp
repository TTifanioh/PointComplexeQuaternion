quaternion quaternion::addition(quaternion &A)
{
    quaternion D;
    D.re = re + A.re;
    D.i = i + A.i;
    D.j = j + A.j;
    D.k = k + A.k;
    return D;
}
quaternion quaternion::soustraction(quaternion &A)
{
    quaternion D;
    D.re = re - A.re;
    D.i = i - A.i;
    D.j = j - A.j;
    D.k = k - A.k;
    return D;
}
quaternion quaternion::multiplication(quaternion &A)
{
    quaternion D;
    D.re = re*A.re - i*A.i - j*A.j - k*A.k;
    D.i = re*A.i + A.re*i + j*A.k - k*A.j;
    D.j = re*A.j - i*A.k + A.re*j + k*A.i;
    D.k = re*A.k + i*A.j -j*A.i + A.j*k;
    return D;
}
